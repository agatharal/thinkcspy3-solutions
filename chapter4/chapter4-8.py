# Write a function area_of_circle(r) which returns the area of a circle of
# radius r.

def area_of_circle(r):
    """Accepts 'r' as a variable for radius then computes and returns the value
        of the area of the circle with radius 'r'."""
    area = 2 * 3.1416 * r  # using formula a_of_circle = 2 * pi * radius

    return area
